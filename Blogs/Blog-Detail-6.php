                                            <h3 class="title">Primary Objective </h3>
                                            <p>The primary objective of the Berne Convention is to design a protection method that can be applied from the very instant that content comes into being. It can be in any form but preferably in physical conditions like a physical storage device or a hardcopy. </p>
                                            <p>The content for copyright may consist of literature, a computer program, build guides, blueprints, movies, artist works, and various other forms of creative content expressed in different forms.</p>
                                            
