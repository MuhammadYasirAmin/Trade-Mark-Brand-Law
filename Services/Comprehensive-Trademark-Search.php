A trademark owner will frequently issue a cease and desist letter to the infringing party, requesting that they stop using the mark. The infringement may not be intentional, and the infringing party may cease to act if they are informed that their actions are clearly infringing on another's rights. The infringing party may potentially refuse to comply with the demand or claim that they are not infringing. You might consider filing a trademark infringement action at that stage. If you believe that filing a lawsuit is the best option for you, you should speak with an attorney. 

